package org.nic.lmd.wenderapp.entities;

public class ThanaEntity {


    private String thanaCode;
    private String thanaName;
    private String policeStationName;
    private String districtId;
    private String admDistrictId;
    private String blockCode;

    public String getThanaCode() {
        return thanaCode;
    }

    public void setThanaCode(String thanaCode) {
        this.thanaCode = thanaCode;
    }

    public String getThanaName() {
        return thanaName;
    }

    public void setThanaName(String thanaName) {
        this.thanaName = thanaName;
    }

    public String getPoliceStationName() {
        return policeStationName;
    }

    public void setPoliceStationName(String policeStationName) {
        this.policeStationName = policeStationName;
    }

    public String getDistrictId() {
        return districtId;
    }

    public void setDistrictId(String districtId) {
        this.districtId = districtId;
    }

    public String getAdmDistrictId() {
        return admDistrictId;
    }

    public void setAdmDistrictId(String admDistrictId) {
        this.admDistrictId = admDistrictId;
    }

    public String getBlockCode() {
        return blockCode;
    }

    public void setBlockCode(String blockCode) {
        this.blockCode = blockCode;
    }
}
